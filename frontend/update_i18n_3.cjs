async function run() {
  for (const [file, reps] of Object.entries(replacements)) {
    const filePath = path.join(srcDir, file);
    if (!fs.existsSync(filePath)) {
      console.log(`File not found: ${filePath}`);
      continue;
    }
    
    let content = fs.readFileSync(filePath, 'utf8');
    for (const {from, to} of reps) {
      // Split the content and join for exact global replacements, except for things that shouldn't be global
      // Wait, simple replace is only first match. Since I need to replace all instances (like "Location" which might appear multiple times), I should use split.join.
      content = content.split(from).join(to);
    }
    
    fs.writeFileSync(filePath, content, 'utf8');
    console.log(`Updated ${file}`);
  }
}

run();
